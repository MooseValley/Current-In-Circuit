public class Ch08_Q50__CurrentInCircuit
{
   public static void main (String [] args)
   {
      // Constants:
      final double e               = 2.718281828;
      final double Amax            = 0.07176;
      final double Tinc            = 1.69E-05;
      final double Tstart          = 0.0;
      final int    numValuesToPlot = 200;
      final double valuesArray []  = new double [numValuesToPlot];

      double Tvalue = Tstart;
      double Afunction = 0.0;

      for (int k = 0; k < numValuesToPlot; k++)
      {
         Afunction = Amax * Math.pow (e, -250.0 * Tvalue) * Math.cos (7061.4 * Tvalue);

         valuesArray [k] = Afunction;

         Tvalue = Tvalue + Tinc;
      }

      System.out.println (BarGraph_Horizontal.BarGraph (valuesArray, null,        ' ', false));


   }
}